            <ol start='1'>
                <li>
                    <h3>Facil de usar</h3>
                    <p>Si un producto es fácil de usar, significa que el diseño, la estructura y el propósito del producto son claros y que el producto es fácil de entender. A la hora de evaluar cuán fácil de usar es un producto, puedes hacerte preguntas como estas: ¿Todas las partes del diseño son fáciles de encontrar? ¿Es fácil entender la funcionalidad del diseño? ¿Pueden los usuarios realizar tareas específicas dentro del diseño? Durante tu evaluación, estas preguntas pueden ayudarte a determinar si el diseño es fácil de usar.</p>
                </li>
                <li>
                    <h3>Equitativo</h3>
                    <p>Si un producto es equitativo, significa que su diseño es útil para personas con diversas capacidades y antecedentes. En otras palabras, el diseño del producto responde a las necesidades de un público diverso y garantiza una experiencia de alta calidad a todos los usuarios, independientemente de su origen, género, raza o nivel de habilidad. La equidad consiste en proporcionar a las personas las herramientas que necesitan para alcanzar sus objetivos y tener una mejor calidad de vida. La equidad no se reduce solo a la igualdad —dar a todos los mismos recursos— porque, a menudo, las personas necesitan herramientas y apoyo diferentes según sus necesidades. Esto es especialmente importante para quienes pertenecen a grupos que suelen ser excluidos.</p>
                </li>
                <li>
                    <h3>Agradable</h3>
                    <p>Si un producto es agradable, significa que el diseño deleita al usuario. El diseño refleja lo que el usuario piensa o siente y crea una conexión positiva con él. El diseño de un producto no tiene por qué ser agradable para que funcione correctamente. Sin embargo, un diseño agradable complementa un producto ya funcional y puede mejorar la sensación del usuario sobre la experiencia.
                    A la hora de evaluar qué tan agradable es la UX de un producto, podrías hacerte preguntas como estas: ¿Hay aspectos del diseño que tengan en cuenta los sentimientos del usuario?  ¿El diseño deleita al usuario? ¿El diseño mantiene la atención del usuario durante toda su experiencia? Estas preguntas pueden ayudarte a determinar si el diseño ofrece una experiencia agradable. 
                    Imagina que estás evaluando cuán agradable es una aplicación de streaming de video. Algunos aspectos del diseño que podrían hacer más agradable el producto son las recomendaciones personalizadas basadas en los canales que el usuario vio anteriormente o la capacidad de personalizar el aspecto de la cuenta.
                    </p>
                </li>
                <li>
                    <h3>Útil</h3>
                    <p>Si un producto es útil, significa que resuelve los problemas del usuario. En otras palabras, el diseño resuelve intencionalmente un problema del usuario que el diseñador ha identificado. Es importante tener en cuenta que, aunque están relacionados, los conceptos «útil» y «fácil de usar» tienen significados diferentes. Un producto útil no siempre es fácil de usar, ni viceversa.
                    La distinción entre ambos es que «fácil de usar» se refiere a que el producto funciona bien y es simple de usar, mientras que «útil» se refiere específicamente a la capacidad de resolver los problemas del usuario. A la hora de evaluar qué tan útil es la UX de un producto, podrías hacerte preguntas como estas: ¿El diseño añade valor a la experiencia del usuario? ¿Soluciona el diseño un problema del usuario? ¿El diseño ayuda al usuario a alcanzar un objetivo específico? Estas preguntas pueden ayudarte a determinar si el diseño ofrece una experiencia útil. 
                    </p>
                </li>
            </ol>
