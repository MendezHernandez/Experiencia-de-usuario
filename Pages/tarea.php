                <li>
                    <h3>Diseñador de interacción</h3>
                    <p>El trabajo de un diseñador de interacción es responder preguntas como las siguientes: ¿Qué debería ocurrir si un usuario toca este botón? ¿Cómo hacemos que realizar esta acción sea más fácil para los usuarios? ¿Cómo se presentan los elementos de diseño dentro del sitio web? Los diseñadores de interacción no se centran tanto en la apariencia del producto, sino, más bien, en hacer que sea fácil navegar por este e interactuar con él.
                    </p>
                </li>
                <li>
                    <h3>Diseñador visual</h3>
                    <p>Los diseñadores visuales se centran en cómo se ve un producto o una tecnología. A menudo, son responsables de diseñar logotipos, ilustraciones e íconos, así como de decidir el color, el tamaño y la ubicación de la fuente. Los diseñadores visuales se centran en el diseño de cada página o pantalla y hacen que todos los elementos del diseño encajen de forma visualmente atractiva. 
                    </p>
                </li>
                <li>
                    <h3>Diseñador de movimiento</h3>
                    <p>Los diseñadores de movimiento analizan qué siente un usuario al desplazarse por un producto y cómo crear transiciones fluidas entre distintas páginas de una aplicación o un sitio web. También pueden crear animaciones o efectos visuales para dar vida a sus ideas de diseño.
                    </p>
                    <p>El trabajo de un diseñador de movimiento es responder preguntas como las siguientes: ¿Cómo debe ser la transición entre distintas páginas de una aplicación? ¿Cómo mostramos la conexión entre estas acciones? ¿Cómo podemos crear una animación atractiva que nos ayude a contar nuestra historia? Los diseñadores de movimiento se centran en los elementos del diseño que se mueven, en lugar de los diseños estáticos tradicionales
                    </p>
                </li>
                <li>
                    <h3>Investigador de UX</h3>
                    <p>Los investigadores de UX llevan a cabo estudios o entrevistas que analizan cómo las personas usan un producto. A menudo, los investigadores de UX identifican las dificultades que experimentan los usuarios y exploran cómo los productos pueden ayudar a resolverlas. También investigan cuán fáciles de usar son los productos actuales, por ejemplo, al pedir a los usuarios que realicen determinadas tareas en una aplicación o un sitio web.
                    </p>
                    <p>Los investigadores de UX responden preguntas como las siguientes: ¿Qué problemas enfrentan los usuarios? ¿El diseño de este producto es fácil de usar? ¿Estaría interesada la gente en esta nueva función de diseño? El objetivo de los investigadores de UX suele ser entender cómo un producto puede proporcionar una solución a un problema real que tienen los usuarios.
                    </p>
                </li>
                <li>
                    <h3>Escritor de UX</h3>
                    <p>Los escritores de UX piensan en cómo hacer que el texto que se usa en un producto sea más claro, de modo que la experiencia del usuario sea más intuitiva. Los escritores de UX también ayudan a definir la voz y la personalidad de una marca. El trabajo de los escritores de UX suele incluir tareas como escribir las etiquetas de los botones y determinar el tono del texto que se usa en una aplicación o un sitio web.
                    </p>
                </li>
                <li>
                    <h3>Ingeniero de UX</h3>
                    <p>Los ingenieros de UX traducen la intención del diseño en una experiencia funcional, como una aplicación o un sitio web. También ayudan a los equipos de UX a saber si los diseños son intuitivos y técnicamente factibles.
                    <br><br>
                    Los ingenieros de UX responden preguntas como las siguientes: ¿Cómo implementamos cada interacción? ¿Cómo materializamos este diseño de forma que se mantenga fiel a su intención original? ¿Cómo podemos explorar alternativas para determinar la forma de ofrecer la mejor experiencia al usuario? Los ingenieros de UX materializan el diseño y el desarrollo y dan vida a productos conceptuales. 
                    </p>
